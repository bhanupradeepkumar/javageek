package javabasic;

public class forLoop {
    public static void main(String[] args) {
        int o;
        for(o = 0; o < 10; o++) {
            System.out.println("gfgbhanu");
        }

        System.out.println(o);

        for(int i = 0; i < 3; i++) {
            System.out.println("bhanu");


        }

    }
}
